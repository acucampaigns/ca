						<p>Canada has implemented a web-based travel verification system for a VISA exempt foreign national that is known as Electronic Travel authorization (eTA). The implementation of this program is just one of the many initiatives the Canadian government is undertaking as part of their joint agreement with the United States to better secure the border they share.</p>
						
						<h3>eTA Filing Is Now Compulsory</h3>
						
						<p>The United States and Canada have adopted a policy designed to improve the security of the borders they share as well as further collaboration which includes the sharing of visa and immigration information they may gather. The enforcement of Electronic Travel Authorisation’s is one of Canada’s initiatives in response to this agreement.</p>
						
						<h3>Basics Of The Canadian eTA</h3>
						
						<p>The Canadian eTA is much like the digital travel verification systems that are in place in many developed countries such as the United States and Australia. Just like its name suggests, the eTA confirms an individual’s suitability for travel to Canada. It is similar to a typical visa and is often referred to as Canada’s ‘mini visa’, however the application for and release of an eTA will be completed entirely online.</p>
						
						<h3>Scope Of The eTA</h3>
						
						<p>Foreign nationals from visa exempt countries will be affected by the eTA system, namely those from select European nations as well as New Zealand and Australia. Because Canadians are not restricted for travel in the United States, it is likely that US citizens will enjoy this same benefit meaning they will not need an eTA to travel to Canada. Foreign nationals who have to apply for visas to travel to Canada will also be exempt from ETA’s as they have their own travel approval process.</p>
						
						<h3>Canadian eTA Application</h3>
						
						<p>As the name implies, filing for and obtaining an eTA will be an electronic, or web based process. Tourists will visit the Citizenship and Immigration Canada (CIC) website where they will need to supply personal information such as their name and birth date and possibly their fingerprints in order to receive their eTA. The authorization is generated via the website, printed out and then submitted to the appropriate immigration officials along with all other travel documentation before departing for Canada.</p>
						
						<h3>eTA System Benefits</h3>
						
						<p>The central purpose of the eTA is to smooth the way for eligible individuals to travel to Canada. Because it will be easier to identify undesirable or inadmissible parties, such as those with no fly orders or criminal records, it will be easier to prevent them from ever entering the country. Thus the manpower and funds that are allotted for sending these individuals back to where they came from will be reduced and in the long run, save the government potentially crucial resources.</p>
						
						<p>Enactment of the eTA program will also accomplish Canada’s allegiance to the shared vision that they and the US have for improving their border security and developing economic competitiveness, which will in turn accelerate the flow of both people and goods while maintaining the strength of their partnership.</p>
						
						<p>It is believed that the necessity for an eTA will deter some foreign nationals who are well aware of their ineligibility from applying for one, especially as certain personal details are required prior to departing from their resident location.</p>
						
						<p>The eTA will also give the CIC a way to gather data and track the travel patterns of foreign nationals who are visa exempt. The government had no way to do this before as there was no pre-screening process for foreign nationals until now.</p>
						
						<p>The eTA program is also an introduction to a new tool which will allow for more distinct and personal risk assessments for foreign national than what was possible with previous visa requirements. With this system in place it is something to be considered when conducting strategic discussions about the visa policy framework and any changes that it is discovered should be implemented.</p>
						
